   RF.LOCRESERVA,
   RF.NUMRESERVA,
   RF.IDRESERVASFRONT,
   RF.IDHOTEL,
   RF.CODUH,
   RF.STATUSRESERVA,
   RF.DATACHEGPREVISTA,
   RF.DATAPARTPREVISTA,
   RF.DATACHEGADAREAL,
   RF.DATAPARTIDAREAL,
   RF.HORAPARTIDAREAL,
