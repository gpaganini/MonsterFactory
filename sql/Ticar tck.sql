  CONTASFRONT CF
    JOIN RESERVASFRONT RF ON
      (CF.IDRESERVASFRONT = RF.IDRESERVASFRONT)
    JOIN LANCAMENTOSFRONT LF ON
      (CF.IDCONTA = LF.IDCONTA)
